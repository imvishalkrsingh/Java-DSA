package BasicsOfJava;

public class VolumeOfSphere {
    public static void main(String[] args) {
        double pie = 3.14;
        double radius = 48;

//        Volume of sphere Formula = (4/3)*pie*r3

        double volume = (4.0/3.0) * pie * radius * radius * radius;
        System.out.println(volume);
    }
}
