package Oops.ClassAndObjects;

public class preDefinedMethods {
    public static void main(String[] args) {

        System.out.print("Ceil Value of 6.9 : ");
        System.out.println(Math.ceil(6.9));

        System.out.print("Floor Value of 5.3 : ");
        System.out.println(Math.floor(5.3));

        System.out.print("Square Root of 24 : ");
        System.out.println(Math.sqrt(24));

        System.out.print("Square Root of 16 : ");
        System.out.println(Math.sqrt(16));



    }
}
